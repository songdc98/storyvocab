// Optional StoryVocab 3000 cloud endpoint template.
// Deploy this shape to your own server, Cloudflare Worker, Vercel Function, or
// any platform that can receive POST JSON and call your preferred model provider.
// Keep provider API keys on the server side only. Do not put secret keys in the browser.

const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Methods": "POST, OPTIONS",
  "Access-Control-Allow-Headers": "Content-Type"
};

export default {
  async fetch(request, env) {
    if (request.method === "OPTIONS") {
      return new Response(null, { headers: corsHeaders });
    }
    if (request.method !== "POST") {
      return json({ error: "Use POST with the StoryVocab payload." }, 405);
    }

    const payload = await request.json();
    const prompt = buildPrompt(payload);
    const storyText = await callYourModelProvider(prompt, payload, env);

    return json({
      storyText,
      notes: "Generated by your own cloud endpoint."
    });
  }
};

function buildPrompt(payload) {
  const wordLines = (payload.words || [])
    .map((item) => `- ${item.display || item.word}: ${item.zh || ""}`)
    .join("\n");
  return [
    `Write a vivid English-learning story for Day ${payload.day}.`,
    `Theme: ${payload.theme}. English density: ${payload.englishDensity}%.`,
    "Use each target word or phrase naturally. Keep the Chinese meaning near the target word.",
    "Return plain text only.",
    "",
    wordLines
  ].join("\n");
}

